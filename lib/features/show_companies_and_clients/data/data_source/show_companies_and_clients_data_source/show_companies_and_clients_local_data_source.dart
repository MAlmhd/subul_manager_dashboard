import 'package:hive/hive.dart';
import 'package:subul_manager_dashboard/core/helpers/constants.dart';
import 'package:subul_manager_dashboard/features/show_companies_and_clients/domain/entities/comanies_and_clients_entity/companies_and_clients_entity.dart';

abstract class ShowCompaniesAndClientsLocalDataSource {
  List<CompaniesAndClientsEntity> showCompaniesAndClients();
}


class ShowCompaniesAndClientsLocalDataSourceImpl implements ShowCompaniesAndClientsLocalDataSource
{
  @override
  List<CompaniesAndClientsEntity> showCompaniesAndClients() {
     var box = Hive.box<CompaniesAndClientsEntity>(kCompaniesAndClients);
    return box.values.toList();
  }
  
}
